def get_added_strength(old_lv, old_strength, new_lv, new_strength):
    if old_lv == new_lv:
        return new_strength - old_strength
    add_strength = 0
    for i in range(old_lv, new_lv + 1):
        nxt_lv, nxt_strength = get_nxt_bond_level_strength(old_lv)
        if i == old_lv:
            add_strength += nxt_strength - old_strength
        elif i == new_lv:
            add_strength += new_strength
        else:
            add_strength += nxt_strength

    return add_strength


def get_new_bond_level_strength(cur_lv, cur_strength, add_strength):
    nxt_lv = cur_lv
    nxt_strength = cur_strength
    iter_lv = cur_lv
    iter_strength = cur_strength
    left_strength = add_strength
    while left_strength > 0:
        lv, strength = get_nxt_bond_level_strength(iter_lv)
        if strength <= 0:
            nxt_lv = iter_lv
            nxt_strength = iter_strength + left_strength
            break
        if iter_strength + left_strength < strength:
            nxt_lv = iter_lv
            iter_strength += left_strength
            nxt_strength = iter_strength
            left_strength = 0
        else:
            iter_lv += 1
            left_strength -= strength - iter_strength
            iter_strength = 0
            nxt_lv = iter_lv
            nxt_strength = iter_strength

    return (nxt_lv, nxt_strength)


